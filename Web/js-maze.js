    function storePlayer(x,y,draw){
        var Y = draw[y]
        var X = Y.split("");
        if(X[x] == "$"){
            X[x] = "&";
            levelX = -99
            levelY = 99
            
        }
        if(X[x] != "#"){
            X[x] = "&";
        }
        
        if(X[x+1] == "#"){
            rightWall = true;
        }else{
            rightWall = false;
        }
        if(X[x-1] == "#"){
            leftwall = true;
        }else{
            leftwall = false;
        }
        var newY = draw[y-1];
        var newx = newY.split("");
        if(newx[x] == "#"){
            roof = true;
        }
        else{
            roof = false;
        }	
        var belowY = draw[y+1];
        var belowx = belowY.split("");
        if(belowx[x] == "#"){
            floor = true;
        }
        else{
            floor = false;
        }	
        
        X  = X.join("");
        draw[y] = X;
    
        return draw;
        
    }
    
    function checkNextLevel(){
        if(!rightWall){
            if(x+1 == 53){
                levelX += 1;
                x = 1;
            }
        }
        if(!leftwall){
            if(x-1 == -1){
                levelX -= 1;
                x = 51;
            }
        }
        if(!roof){
            if(y-1 == -1){
                levelY += 1;
                y = 19;
                return;
            }
        }
        if(!floor){
            if(y+1 == 21){
                levelY -= 1;
                y = 1;
            }
        }
        
    }
    
